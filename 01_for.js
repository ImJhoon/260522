// for (초기식; 조건식; 증감식)
for (let i = 0; i < 5; i++) {
  console.log(i);
}

// 배열 순회
const arr = ["축구", "농구", "배구", "야구"];

for (let i = 0; i < arr.length; i++) {
  // 0부터 시작해서 길이 미만으로 조건식을 작성
  // 1부터 시작을 하면 모든 조건이 '충족' -> 전체 길이 이하여야 모든 원소/요소를 찾을 수 있음
  // 인덱스가 0부터 시작하기 때문에 초기 변수 값도 0에서 시작하는 편이..
  if (arr[i] == "배구") {
    continue;
  }

  console.log(arr[i]);
}
